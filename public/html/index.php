<p>最近有个沙雕视频在网络<a href="javascript:(void);">上火</a>了。</p>
<p>说一个<a href="javascript:(void);">学霸</a>模样的<strong>乖乖女</strong>在放学途中被<strong>四名不良少年</strong>拦下。</p>
<p><span id="article-img-0" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/f08b1af2968c5ab594f4738592045a402.jpg"></span></p>
<p>但他们一不劫财，二不劫色，而是恶狠狠地问了一句：</p>
<p><strong>同学，你知道同心圆吗？</strong></p>
<p><span id="article-img-1" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/aeadf34766ca22047dca55fc7d14f0ed2.gif"></span></p>
<p>阿嘞嘞？现在的不良少年都已经这么热血学习了吗？！<br></p>
<p>原来，这群不良少年捡到了一本<strong>《黑魔法秘籍》</strong>，想要学上面的法术救活他们的老大。</p>
<p><span id="article-img-2" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/cdd489a69ec55fb975b959c580fd7f702.jpg"></span></p>
<p>可秘籍上的第一步骤他们就看不懂：</p>
<p>在两个同心圆的内圆里内接<a href="javascript:(void);">五芒星</a>，顶点指向北方。</p>
<p><span id="article-img-3" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/64cae72ce56dc992904e89f8d41ca4882.jpg"></span></p>
<p>同心圆？内圆？<a href="javascript:(void);">五芒星</a>？这都哪儿跟哪儿啊。<br></p>
