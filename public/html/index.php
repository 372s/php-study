<p>果然是只有岛国人民才能想得到的沙雕剧情走向啊。<br></p>
<p>这个故事名叫《<a href="javascript:(void);">数学的傍晚</a>》，正是出自今年份的——</p>
<p><strong>《世界奇妙物语》</strong></p>
<p><strong>2018秋季特别篇</strong></p>
<p><strong><span id="article-img-9" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/e4153f482fc783e7896706643d8818cf2.jpg"></span></strong></p>
<p>这部剧鱼叔<strong>每年都会推荐两次</strong>。</p>
<p>无论收视时高时低，无论口碑时好时坏，我都始终不离不弃，坚持要追到停更为止。</p>
<p>作为全世界最长寿的电视剧，《世奇》从1990年开播至今，已经连载<strong>整整28年</strong>了。</p>
<p>早已经成为全<a href="javascript:(void);">日本</a>，甚至全世界家喻户晓的一部电视剧。</p>
<p>该剧的播出方式，从最初的一周一集，改为现在每年推出<strong>春夏特别篇</strong>；</p>
<p>但负责串场的<strong><a href="javascript:(void);">森田一义</a></strong>，和那副<strong>万年不变的墨镜</strong>，却始终不曾变过。（据说<a href="javascript:(void);">王家卫</a>总是戴着墨镜，就是在模仿他）</p>
<p>今年<a href="javascript:(void);">森田一义</a>已经73岁高龄。</p>
<p><span id="article-img-10" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/f7ca64acd0d5d18589db5f6f71f566bd2.jpg"></span></p>
<p>鱼叔当然希望老爷子长命百岁，但还是不免担心，如果有一天他去了，《世奇》将该如何继续。<br></p>
<p>抱歉抱歉，最近伤心事太多，难免有些悲观。</p>
<p>《世奇》能够横行电视界而经久不衰，靠的是精妙绝伦的反转和细思极恐的设计。</p>
<p>在玩脑洞和创意方面，都极具<strong>超前意识</strong>。</p>
<p><strong>《黑镜》和《9号秘事》都得喊它一声老师。</strong></p>
<p>比如《黑镜》在<strong>2014年</strong>推出的《圣诞特别篇》，里面现实一分钟感觉一千年的时间监狱创意，早在<strong>1998年</strong>《世界奇妙物语秋季SP》的《惩罚役30天》中就已经出现。</p>
<p><span id="article-img-11" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/c2bf1613e50428b176bb287f9f8a4a872.jpg"></span></p>
<p>网上甚至有铁杆粉整理过两部剧重合的创意，<strong>《黑镜》中的不少梗都是《世奇》早几年前就玩剩下的。</strong></p>
<p>今年的《世奇》一共分了5个小故事，开头提到的《<a href="javascript:(void);">数学的傍晚</a>》就是其中之一。</p>
<p>也算是呼应了今年沙雕剧的大热现象。</p>
<p>剩下的几个，鱼叔下面给大家一一介绍：</p>
<p><strong>《无法逃脱》</strong></p>
<p>这个故事可以简单概括为<strong>「吃瓜版《<a href="javascript:(void);">电锯惊魂</a>》」</strong>。</p>
<p>说男主角（坂口健太郎 饰）一觉醒来，发现自己被关在密室之中。</p>
<p><span id="article-img-12" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/7e0c98a7da31a3169803d8fc679910662.jpg"></span></p>
<p>开门密码的线索都在房间之中，找不到，就会被活活闷死。<br></p>
<p>他可以通过和网友们互动获得场外援助，集众人之智，找到逃生方法。</p>
<p><span id="article-img-13" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/f1dfdc77d9e99d5c8fb9ca0dad75c4ef2.jpg"></span></p>
<p>当然，也可能招致反效果。<br></p>
<p>这个故事比较老套，用脚指头想想就能猜到，男主一定是犯了什么错事遭到他人报复。</p>
<p><span id="article-img-14" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/54e4b9ca48ddc731c8a64588da6261022.jpg"></span></p>
<p>同时结合了最近很热的「屏幕电影」概念，穿插了视频直播形式。</p>
<p>所以鱼叔也不想多讲了，只能提示大家一句：</p>
<p><strong>吃瓜有风险，围观需谨慎。</strong></p>
<span id="article-img-15" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/01fe9c972f7a8dc91f672b2d39e2f9872.jpg"></span><p><strong>《明天的我》</strong></p>
<p>第二个故事鱼叔就比较喜欢了。</p>
<p>想要预知未来算得上是人类的本能。</p>
<p>但问题就在于，如果有机会，你是否真的愿意知晓未来，并承受受随之而来的代价与后果。</p>
<p><span id="article-img-16" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/269723a276e91939b4bfaef59cd4791c2.jpg"></span></p>
<p>这个主题《世奇》已经用过很多次了，但这回的方式却最为抓马——<br></p>
<p>直接给你发预告片。</p>
<p><span id="article-img-17" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/2287d685322f6ef8283ecfe934b115d82.jpg"></span></p>
<p>女主角（国仲凉子 饰）是个普通日本主妇，每天的生活两点一线，枯燥且乏味。<br></p>
<p><span id="article-img-18" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/38d0f1ee640a74d3702753be3fa7654b2.jpg"></span></p>
<p>她的丈夫忙于工作、日常加班，赚的钱不多，花钱也很斤斤计较。<br></p>
<p><span id="article-img-19" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/df84516914785485e233a79e3365c3842.jpg"></span></p>
<p>某天，女主收到了一条名为<strong>「明天的你」</strong>的短片，预告了她明天即将邂逅自己<strong>曾经的初恋。</strong></p>
<p>初恋现在已经当上主任医师，有钱有闲，还长得挺拔帅气。</p>
<p><span id="article-img-20" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/7d2dd5638bd51680fef56079b7d042062.jpg"></span></p>
<p>比起自家胡子拉碴的老公来说，不知好多少。<br></p>
<p>预告片中的事情第二天真的都一一发生。</p>
<p>之后，女主每天都会准时准点收到预告片，<strong>一档「婚外禁忌之恋」<a href="javascript:(void);">肥皂</a>泡沫剧就此上演。</strong></p>
<p><span id="article-img-21" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/81c373128e4dcaef2bc734049f5cc88f2.gif"></span></p>
<p>但<strong>预告片的套路</strong>大家都懂，全剧透就没意思了，必定是摘选情节高潮点，<a href="javascript:(void);">孤儿</a>剪辑加点悬念，吊足人胃口。<br></p>
<p>比如女主从预告片中看到，初恋明天会约自己去海边，还口口声声说「我喜欢」。</p>
<span id="article-img-22" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/db1d9cc99c02e4678a866dfd7048dfa72.gif"></span><p>甚至脸都凑过来要亲上了！</p>
<span id="article-img-23" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/e63dff1dcdff0153885b33489e8a40612.jpg"></span><p>实锤了实锤了，女主捂脸跑走。<br></p>
<p>结果第二天，海边约会倒是真的，但「我喜欢……」后面接的是<strong>路边小吃</strong>；</p>
<p><span id="article-img-24" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/2354345babd168f8c9a2beaeb8921d8e2.jpg"></span></p>
<p>脸凑过来是因为女主脸上<strong>有脏东西</strong>要擦掉。<br></p>
<p><span id="article-img-25" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/632a6d4bfbb7de87a69d7d21bb71fac62.jpg"></span></p>
<p><strong>女主内心<a href="javascript:(void);">OS</a>：</strong></p>
<p><strong>呀巴哩都是剪辑师的恶作剧！<br></strong></p>
<p>尽管预告片有夸张之嫌，但总体剧情走向还是八九不离十，婚外情真的发生了。</p>
<p>二度开春的女主<strong>沉迷预告片</strong>，越来越偏离原本的生活轨迹。</p>
<p><span id="article-img-26" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/41ac4bbb502095a7af44d61cc4944d282.jpg"></span></p>
<p>直到她收到了<strong>《最终章》</strong>的预告——<br></p>
<p><span id="article-img-27" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/4718ea716bf08f9eeeb40d98479374a42.jpg"></span></p>
<p>说明天初恋会约女主私奔，但与此同时丈夫会在工地遭遇意外。<br></p>
<p>而且<strong>好死不死剪了个心跳骤停的镜头</strong>。</p>
<p><span id="article-img-28" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/1e135d6f8ba5c6928d59001f9ca7c0102.gif"></span></p>
<p>（上帝视角简直作弊）<br></p>
<p>毕竟一日夫妻百日恩，女主放弃私奔，还是选择赶去了医院。</p>
<p>结果却发现丈夫只是<a href="javascript:(void);">骨折</a>而已。</p>
<p><span id="article-img-29" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/c9abbb30ecb59ab6ac19b6c30f3647ec2.jpg"></span></p>
<p>本以为故事将以妻子的回归家庭作为结局完结撒花；<br></p>
<p>没想到半路杀出个<strong>初恋的原配</strong>，把小三女主给捅了（真的是非常狗血的八点档剧情了）。</p>
<p><span id="article-img-30" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/419330eae0989d25e24f9cf6c7bd86682.jpg"></span></p>
<p>然而故事至此仍没有结束，<strong>又一款预告片上线</strong>：</p>
<p>女主将被改造成机械人满血复活，一场轰轰烈烈的血腥屠杀即将开场……</p>
<p><span id="article-img-31" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/c1fa4ca474ffa3bfa51be6d4001e32662.gif"></span></p>
<p>预知后事如何，敬请期待<strong>番外大电影《妻子的复仇》</strong>！</p>
<p><span id="article-img-32" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/31da8c23f7e12d93d98ce7555b0e7ff82.jpg"></span></p>
<p>很扯对吧，非常扯。</p>
<p>只要脑洞足够大，这个故事可以无限期地拍下去。<br></p>
<p>但是醒醒吧，生活并不是电视剧，你无法预知未来，更不可能规避死亡。</p>
<p><span id="article-img-33" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/0db5ed5eb0e3a199d9599d963fc3f8a62.jpg"></span></p>
<p><strong>《幽灵社员》</strong></p>
<p>「幽灵社员」有两种解释：</p>
<p>一种是指公司里<strong>存在感几乎为零、不求<a href="javascript:(void);">升职</a><a href="javascript:(void);">加薪</a>只坐等退休</strong>的隐形员工；</p>
<p><span id="article-img-34" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/732174fd233ae2d5eea5e5f0fde3fb852.jpg"></span></p>
<p>一种则是真的是已经<strong>变成「鬼」的已故员工</strong>。<br></p>
<p><span id="article-img-35" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/4c78c5c8d716696bcdb438b669df8d912.jpg"></span></p>
<p>故事中，勤奋工作过劳死的「幽灵社员A」因为<strong>心系未完成的项目而不愿升天</strong>每天徘徊在在公司里成了游魂野鬼。<br></p>
<p>而偏偏每天数着日子等退休的「幽灵社员B」，却是唯一一个能看到鬼魂的人。</p>
<p><span id="article-img-36" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/21249cce83d932a286b278535f98b7c72.jpg"></span></p>
<p>于是，A缠上了B，希望他能够帮助自己完成遗愿……<br></p>
<p><span id="article-img-37" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/ed1aff4ea5ff73c4c2b07cb49f7f59882.jpg"></span></p>
<p>不说别的，这家公司的老板一定是祖坟冒青烟了，才能遇到这种死了都还要工作的员工。<br></p>
<p>生是你的员工，死也是你的帮工。</p>
<p>这个故事似乎是在正面赞美员工为公司尽心尽责，死了都不忘加班；</p>
<p>但实际上却在讽刺现代社会公司过分压榨人气，<strong>过劳死频发的社会现象</strong>。</p>
<p><span id="article-img-38" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/f6ff88df36198456997eb299608333ad2.gif"></span></p>
<p><strong>珍爱生命，远离<a href="javascript:(void);">工作狂</a>，除非你也想变成「幽灵员工」。</strong></p>
<p><strong>《圣诞的怪物》</strong></p>
<p>最后这个故事也是典型《世奇》套路，和去年秋季篇的《寺岛》很相似。</p>
<p>都是聚焦<strong>校园<a href="javascript:(void);">暴力</a>问题</strong>，都是以「我有一个奇怪的朋友」作为叙述方式。</p>
<p><span id="article-img-39" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/99cc5ccdce3bba065315d96db09dfb082.jpg"></span></p>
<span id="article-img-40" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/10e3924e42302b2d9f90709d48589b3a2.jpg"></span><p>甚至连吓人的镜头都一样。<br></p>
<p><span id="article-img-41" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/fbdb9b782f9370fc60be6df5cc3d37142.jpg"></span></p>
<span id="article-img-42" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/ac82f151b4fd8de4eea6e857fe75b2b72.jpg"></span><p>上：《圣诞节的怪物》<br></p>
<p>下：《寺岛》</p>
<p>亮点在于，今年的故事中有个特殊角色：</p>
<p><strong>暗黑版<a href="javascript:(void);">圣诞老人</a>。</strong></p>
<p><span id="article-img-43" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/903cefa6ceb2a13cba156f6f0fd163192.jpg"></span></p>
<p>且扮演者是90年出生的<strong><a href="javascript:(void);">本乡奏多</a></strong>，大概是史上最嫩圣诞老人了。<br></p>
<p><span id="article-img-44" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/30523c274c69c4c5ed04670bc4673a8e2.jpg"></span></p>
<p>在童话故事中，圣诞老人都是面慈心善，会在圣诞节架着麋鹿给孩子们送礼物。</p>
<p>但《世奇》的编剧们脑回路都跟一般人不一样：</p>
<p>好孩子会得到礼物，<strong>坏孩子就该得到惩罚。</strong></p>
<p>人在做，<a href="javascript:(void);">圣诞老人</a>在看。当圣诞钟声响起的时候，该来的报应都会来的……</p>
<p><span id="article-img-45" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/c6e278ef0da42d6b6537a2de7b844cab2.jpg"></span></p>
<p>总的来说，今年份的《世奇秋季SP》算得上是鱼叔印象中最欢乐的一期。<br></p>
<p><strong>非但不吓人，而且还很搞笑</strong>，甚至连往常转场的恐怖洗眼小故事都没有了。</p>
<p>不可否认的是，老剧连载至今，难免有些力不从心，这两年《世奇》的口碑也确实一般。</p>
<p>胖虎也拿不动刀了。</p>
<p>之所以继续追，倒也不全是情怀使然，确实会有一些比较新奇的视角和故事。</p>
<span id="article-img-46" class="a-image"><img src="http://tt.lkeji.com/uploads/181120/49d8fb0e43645f806f27d4898cafc2fe2.jpg"></span><p>只不过可能完成度不够高，更像是一种实验片。</p>
<p>作为一种意义特殊的集体创作形式，《世奇》这些年来更多是在借助自身的影响力来挖掘出一些优秀的新人编剧。</p>
<p>近年来的创作手法，也是从新人编剧中征集出一些或脑洞或感人或黑暗的故事拍摄成片。</p>
<p>大家如果有心盘点就会发现，不管是名声在外的大导大咖，还是初出茅庐的新人新角，很多都参与过《世奇》。</p>
<p>它就是<strong><a href="javascript:(void);">日本</a>电视历上的一枚活化石</strong>。</p>
<p>哪怕近些年总被吐槽创意平平，但每期也都总还是能找到不少亮点值得关注。</p>
<p>这时候，评分反而显得不那么重要了。</p>
<p>鼓励创意，才是这部剧真正的存在价值。</p>
<p><strong>只要这部剧一年一停更，我就还会继续追下去。</strong></p>
